// function to compare the robot efficiency between two robots.
const compareRobots = (robot1, memory1, robot2, memory2) => {
  // step counter
  const getSteps = (state, robot, memory) => {
    // loop to increase steps untill there is no parcels left
    for (let steps = 0; ; steps++) {
      if (state.parcels.length == 0) {
        return steps;
      }
      // robot data
      // action contains direction and memory data
      let action = robot(state, memory);
      state = state.move(action.direction);
      memory = action.memory;
    }
  };

  // set both robot steps to 0
  let steps1 = 0,
    steps2 = 0;

  // loop to run the robots 100 times
  // with random route in order to get better comparsion
  for (let i = 0; i < 100; i++) {
    let task = VillageState.random();
    // call for steps to get total amount of steps from 100 runs
    steps1 += getSteps(task, robot1, memory1);
    steps2 += getSteps(task, robot2, memory2);
  }
  // log the total steps / 100 to get avg from 100 runs
  console.log(`Robot 1 (route robot): ${steps1 / 100} steps`);
  console.log(`Robot 2 (goal orientated robot): ${steps2 / 100} steps`);
};

